def test_render_global_pgm_exists():
    """render_global_pgm is importable and callable."""
    assert callable(raster.render_global_pgm)


def test_render_global_pgm_returns_bytes():
    """render_global_pgm returns bytes of exactly W*H."""
    work = [(0.0, 0.0), (5.0, 0.0), (5.0, 4.0), (0.0, 4.0)]
    areas = {"map0_work.csv": work}
    bounds = raster.grid_bounds({"w": work})
    body = raster.render_global_pgm(areas, bounds)
    w, h = raster.grid_size(bounds)
    assert isinstance(body, bytes)
    assert len(body) == w * h


def test_render_global_pgm_binary_values_only():
    """All pixel values must be FREE (254) or OCCUPIED (0) — no 205-unknown."""
    work = [(0.0, 0.0), (6.0, 0.0), (6.0, 6.0), (0.0, 6.0)]
    areas = {"map0_work.csv": work}
    bounds = raster.grid_bounds({"w": work})
    body = raster.render_global_pgm(areas, bounds)
    arr = np.frombuffer(body, dtype=np.uint8)
    unique = set(arr.tolist())
    assert unique <= {0, 254}, f"Unexpected pixel values: {unique - {0, 254}}"


def test_render_global_pgm_has_free_pixels():
    """A simple work polygon should produce some FREE pixels."""
    work = [(0.0, 0.0), (4.0, 0.0), (4.0, 3.0), (0.0, 3.0)]
    areas = {"map0_work.csv": work}
    bounds = raster.grid_bounds({"w": work})
    body = raster.render_global_pgm(areas, bounds)
    assert 254 in body, "No FREE pixels were rendered"


def test_render_global_pgm_unicom_adds_free():
    """A unicom corridor should add FREE pixels between two work zones."""
    work0 = [(0.0, 0.0), (2.0, 0.0), (2.0, 2.0), (0.0, 2.0)]
    work1 = [(4.0, 0.0), (6.0, 0.0), (6.0, 2.0), (4.0, 2.0)]
    corridor = [(2.0, 0.8), (4.0, 0.8), (4.0, 1.2), (2.0, 1.2)]
    areas_no_corr = {
        "map0_work.csv": work0,
        "map1_work.csv": work1,
    }
    areas_with_corr = {
        "map0_work.csv": work0,
        "map1_work.csv": work1,
        "map0tomap1_0_unicom.csv": corridor,
    }
    # Use union bounds for both
    all_pts = work0 + work1 + corridor
    bounds = raster.grid_bounds({"all": all_pts})

    body_no = raster.render_global_pgm(areas_no_corr, bounds)
    body_yes = raster.render_global_pgm(areas_with_corr, bounds)

    free_no = body_no.count(bytes([254]))
    free_yes = body_yes.count(bytes([254]))
    assert free_yes > free_no, (
        f"Unicom corridor did not add FREE pixels: {free_yes} <= {free_no}"
    )


def test_render_global_pgm_obstacle_carves_free():
    """An obstacle inside a work area should reduce FREE pixel count."""
    work = [(0.0, 0.0), (6.0, 0.0), (6.0, 6.0), (0.0, 6.0)]
    obstacle = [(2.0, 2.0), (4.0, 2.0), (4.0, 4.0), (2.0, 4.0)]
    bounds = raster.grid_bounds({"w": work})

    body_no_obs = raster.render_global_pgm({"map0_work.csv": work}, bounds)
    body_with_obs = raster.render_global_pgm(
        {"map0_work.csv": work, "map0_0_obstacle.csv": obstacle}, bounds
    )

    free_no = body_no_obs.count(bytes([254]))
    free_yes = body_with_obs.count(bytes([254]))
    assert free_yes < free_no, (
        f"Obstacle did not reduce FREE count: {free_yes} >= {free_no}"
    )


def test_render_global_pgm_multi_work_union():
    """Multiple work polygons should all contribute FREE pixels."""
    work0 = [(0.0, 0.0), (3.0, 0.0), (3.0, 3.0), (0.0, 3.0)]
    work1 = [(5.0, 0.0), (8.0, 0.0), (8.0, 3.0), (5.0, 3.0)]
    areas = {"map0_work.csv": work0, "map1_work.csv": work1}
    bounds = raster.grid_bounds({"all": work0 + work1})

    body_both = raster.render_global_pgm(areas, bounds)
    body_only0 = raster.render_global_pgm({"map0_work.csv": work0}, bounds)
    body_only1 = raster.render_global_pgm({"map1_work.csv": work1}, bounds)

    free_both = body_both.count(bytes([254]))
    free_0 = body_only0.count(bytes([254]))
    free_1 = body_only1.count(bytes([254]))
    # Union should have more free than either alone
    assert free_both > free_0, "Second work zone did not add free pixels"
    assert free_both > free_1, "First work zone did not add free pixels"


def test_render_global_pgm_pgm_bytes_wraps_correctly():
    """pgm_bytes() wraps render_global_pgm output into a valid PGM."""
    work = [(1.0, 1.0), (3.0, 1.0), (3.0, 3.0), (1.0, 3.0)]
    areas = {"map0_work.csv": work}
    bounds = raster.grid_bounds({"w": work})
    w, h = raster.grid_size(bounds)
    body = raster.render_global_pgm(areas, bounds)
    pgm = raster.pgm_bytes(w, h, body)
    assert pgm.startswith(b"P5\n# CREATOR: map_generator.cpp 0.050 m/pix\n")
    assert f"{w} {h}".encode() in pgm


