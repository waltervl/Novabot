"""Tests for render_global_pgm() — global union occupancy-grid (Task 8).

render_global_pgm() implements the firmware's global map.pgm rendering
(MapGenerator::saveMap for the whole-map case):
  1. All OCCUPIED (0).
  2. Each work polygon inflated 0.20 m (ClipperLib JT_ROUND) -> FREE (254).
  3. Each unicom/corridor polygon -> FREE (254).
  4. Each obstacle polygon -> OCCUPIED (0).
  5. Return raw pixel bytes.

Byte-exact reproduction of the corpus golden map.pgm is NOT achievable from
x3 CSV inputs alone (RE doc section 8 — the firmware rasterises its in-memory
polygon, which differs ~1-4 cells from the stored x3 CSV; the corpus golden
also has the seam-fix navfix applied). Practical fidelity from x3: ~92%.

The corpus golden was generated by the stock firmware. Its exact canvas
(379x257 at origin [-4.75, -1.60]) requires passing the known-correct
firmware bounds (-3.75, -0.63, 13.20, 10.22) — these are the
post-SimplifyPolygons+expandPolygon bbox. See test_raster_header.py.

Structural tests (always-pass) verify the algorithm is correctly wired.
Corpus tests use the hardcoded known-correct bounds and check:
  - Correct canvas size (379x257) and header
  - Only FREE/OCCUPIED values (binary grid, no 205-unknown)
  - HIGH pixel fidelity against the corpus golden (>=90%)
  - Correct fill order: obstacles carve into free areas
"""
import tarfile
import tempfile
from pathlib import Path

import numpy as np
import pytest

from open_mapping.core import geometry as g, raster, mapfiles

FX = (
    Path(__file__).resolve().parent.parent
    / "harness" / "fixtures" / "corpus" / "multimap_map2"
)

# Known-correct corpus bounds from test_raster_header.py.
# These are the post-SimplifyPolygons+expandPolygon bbox that the stock saveMap
# receives for the multimap_map2 / simple_map1 corpus group (same physical mower).
# grid_bounds(works) from raw x3 CSVs gives (-3.83, -0.33, 12.91, 9.96) → 375x246,
# which differs ~4 cols / ~11 rows from the firmware canvas.
CORPUS_BOUNDS = (-3.75, -0.63, 13.20, 10.22)


def _load_x3_areas(tmp_root: Path):
    """Extract input mapdir_before.tar and return read_x3_areas result."""
    in_dir = tmp_root / "input"
    with tarfile.open(FX / "input" / "mapdir_before.tar", "r") as t:
        t.extractall(in_dir)
    return mapfiles.read_x3_areas(in_dir)


def _load_golden_map_pgm(tmp_root: Path):
    """Extract golden mapdir_after.tar and return map.pgm pixel bytes."""
    out_dir = tmp_root / "golden"
    with tarfile.open(FX / "golden" / "mapdir_after.tar", "r") as t:
        t.extractall(out_dir)
    data = (out_dir / "map.pgm").read_bytes()
    pos = 0
    for _ in range(4):
        pos = data.index(b"\n", pos) + 1
    return data[pos:]


# ---------------------------------------------------------------------------
# Structural tests (non-corpus, always pass)
